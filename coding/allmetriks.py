# ------------------------------------------------------------
# Метрики
# ------------------------------------------------------------
def rmse(a,b):
    return np.sqrt(np.mean((a-b)**2))

def psnr(a,b):
    mse=np.mean((a-b)**2)
    if mse==0:
        return 100
    return 10*np.log10((255**2)/mse)

def resolution(img):
    gray=cv2.cvtColor(img.astype(np.uint8),cv2.COLOR_RGB2GRAY)
    gx=cv2.Sobel(gray,cv2.CV_64F,1,0,ksize=3)
    gy=cv2.Sobel(gray,cv2.CV_64F,0,1,ksize=3)
    return np.mean(np.sqrt(gx**2+gy**2))

# ------------------------------------------------------------
# Обработка
# ------------------------------------------------------------
results_dict = {}

for file in os.listdir():
    if not (file.startswith("result_") and file.endswith(".png")):
        continue

    match = re.findall(r'\d+_\d+', file)
    if not match:
        continue

    aperture = match[0].replace('_','x')

    img=cv2.imread(file)
    img=cv2.cvtColor(img,cv2.COLOR_BGR2RGB).astype(np.float64)

    if img.shape[:2]!=(h0,w0):
        img=cv2.resize(img,(w0,h0))

    results_dict[aperture]=[
        psnr(original,img),
        rmse(original,img),
        resolution(img)
    ]

# ------------------------------------------------------------
# Формирование таблицы строго по порядку
# ------------------------------------------------------------
final_rows=[]

for ap in ordered_apertures:
    ps,rm,res = results_dict.get(ap,(None,None,None))
    k,speed = experiment_data.get(ap,(None,None))

    final_rows.append([ap,ps,rm,res,speed,k])

df=pd.DataFrame(final_rows,columns=[
"Апертура","ПОСШ (дБ)","СКО",
"Разрешающая способность","Быстродействие (с)","k"
])

